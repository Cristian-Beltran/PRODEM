<script>
import { useVuelidate } from "@vuelidate/core";
import { required, helpers, minLength, email } from "@vuelidate/validators";
import {
  createUserRequest,
  getUserRequest,
  updateUserRequest,
} from "../../../api/user";

export default {
  setup() {
    return { v$: useVuelidate() };
  },

  data() {
    return {
      formData: {
        first_name: "",
        last_name: "",
        email: "",
        username: "",
        ci: "",
        telf: "",
        address: "",
        type: "",
        birthdate: "",
      },
      errors: [],
      roles: [],
      alertOpen: false,
    };
  },
  validations() {
    return {
      formData: {
        email: {
          required: helpers.withMessage("Campo requerido", required),
          email: helpers.withMessage("El correo no es valido", email),
        },
        first_name: {
          required: helpers.withMessage("Campo requerido", required),
        },
        last_name: {
          required: helpers.withMessage("Campo requerido", required),
        },
        username: {
          required: helpers.withMessage("Campo requerido", required),
        },
        ci: {
          required: helpers.withMessage("Campo requerido", required),
        },
        address: {},
        telf: {
          required: helpers.withMessage("Campo requerido", required),
          minLength: helpers.withMessage(
            ({ $params }) => `Ingrese un numero valido`,
            minLength(8)
          ),
        },
        type: {},
        birthdate: {
          required: helpers.withMessage("Campo requerido", required),
        },
      },
    };
  },
  methods: {
    notification() {
      this.alertOpen = true;
      const timer = setTimeout(() => {
        this.alertOpen = false;
      }, 3000);
      return () => clearTimeout(timer);
    },
    handleSubmit(event) {
      event.preventDefault();
      this.v$.$touch();
      if (!this.v$.$invalid) {
        const request = async () => {
          try {
            if (!this.$route.query.id) {
              await createUserRequest(this.formData);
            } else await updateUserRequest(this.$route.query.id, this.formData);
            this.$router.go(-1);
          } catch (error) {
            this.errors = error.response.data.errors;
            this.notification();
          }
        };
        request();
      }
    },
  },
  async created() {
    if (this.$route.query.type) this.formData.type = this.$route.query.type;
    if (this.$route.query.id) {
      const res = await getUserRequest(this.$route.query.id);
      this.formData = res.data;
      this.formData.telf = parseInt(this.formData.telf);
      this.formData.birthdate = new Date(this.formData.birthdate)
        .toISOString()
        .slice(0, 10);
    }
  },
};
</script>
